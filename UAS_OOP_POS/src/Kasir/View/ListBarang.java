    private void initTableModel() {
        model = new DefaultTableModel();
        model.addColumn("ID Produk");
        model.addColumn("ID Supplier");
        model.addColumn("Kode Produk");
        model.addColumn("Nama Produk");
        model.addColumn("Harga Jual");
        jTable1.setModel(model);
    }

    private void loadData() {
        model.setRowCount(0); // Clear table
        String sql = "SELECT * FROM product";

        try (Connection c = DBConnection.getConnection();
             Statement s = c.createStatement();
             ResultSet r = s.executeQuery(sql)) {

            while (r.next()) {
                model.addRow(new Object[]{
                    r.getString("id_product"),
                    r.getString("id_supplier"),
                    r.getString("product_code"),
                    r.getString("product_name"),
                    r.getString("price_sell")
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }
    }

    private void searchData() {
        model.setRowCount(0);
        String keyword = txCari.getText();
        String sql = "SELECT * FROM product WHERE product_code LIKE ? OR product_name LIKE ?";

        try (Connection c = DBConnection.getConnection();
             PreparedStatement ps = c.prepareStatement(sql)) {

            String like = "%" + keyword + "%";
            ps.setString(1, like);
            ps.setString(2, like);

            try (ResultSet rs = ps.executeQuery()) {
                while (rs.next()) {
                    model.addRow(new Object[]{
                        rs.getString("id_product"),
                        rs.getString("id_supplier"),
                        rs.getString("product_code"),
                        rs.getString("product_name"),
                        rs.getString("price_sell")
                    });
                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Cari Data Error: " + e.getMessage());
        }
    }

    private void pilihBarang() {
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih salah satu data terlebih dahulu.");
            return;
        }

        String id = model.getValueAt(row, 0).toString();
        String nama = model.getValueAt(row, 3).toString();
        String harga = model.getValueAt(row, 4).toString();

        PenjualanView.txIDBarang.setText(id);
        PenjualanView.txNamaBarang.setText(nama);
        PenjualanView.txHarga.setText(harga);

        dispose();
    }

