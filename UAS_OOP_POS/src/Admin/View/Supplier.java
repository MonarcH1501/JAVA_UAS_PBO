    private void add_suppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_add_suppActionPerformed
    String cd_sp = code_supp.getText();
    String nm_sp = name_supp.getText();
    String cn_sp = contact_supp.getText(); 
    String ad_sp = address_supp.getText();
    
    if(cd_sp.isEmpty() || nm_sp.isEmpty() || cn_sp.isEmpty() || ad_sp.isEmpty()){
        JOptionPane.showMessageDialog(this, "Please fill in all fields", "Input Error", JOptionPane.ERROR_MESSAGE);
    } else {
    
    Supp addSupp = new Supp(0,cd_sp, nm_sp, cn_sp, ad_sp);
    SuppDAO suppDAO;
    
    try {
    suppDAO = new SuppDAO();
    int res = suppDAO.insertSupplier(addSupp);
    if (res == 1) {
        JOptionPane.showMessageDialog(this, "Supplier created successfully ", "Success", JOptionPane.INFORMATION_MESSAGE);
        this.clearForm();
        loadSupplierData();
    } else {
        JOptionPane.showMessageDialog(this, "Error occurred while inserting ", "Error", JOptionPane.ERROR_MESSAGE);
    }
    } catch (SQLException ex){
        JOptionPane.showMessageDialog(this, "Error occurred while inserting", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }
    }//GEN-LAST:event_add_suppActionPerformed

