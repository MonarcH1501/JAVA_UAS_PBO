    private void loadSupplierData() throws SQLException{
        SuppDAO suppDAO = new SuppDAO();
        List<Supp> suppliers = suppDAO.getSuppliers();
        String[][] data = new String[suppliers.size()][5];
        
        for(int i = 0; i < suppliers.size(); i++) {
        data[i][0] = String.valueOf(suppliers.get(i).getId());
        data[i][1] = suppliers.get(i).getCodeSupplier();
        data[i][2] = suppliers.get(i).getNameSupplier();
        data[i][3] = suppliers.get(i).getContactSupplier();
        data[i][4] = suppliers.get(i).getAddressSupplier();
    }
        String[] columnName = {"ID", "Kode Supplier", "Nama Supplier", "Contact", "Address"};
        table_supp.setModel(new javax.swing.table.DefaultTableModel(data, columnName));
    }
    
