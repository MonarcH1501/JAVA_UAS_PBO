    private void del_suppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_del_suppActionPerformed
   if(selectedSuppId != -1){
        int confirmation = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this Supplier?", "Confirm Deletion", JOptionPane.YES_NO_OPTION);
        if(confirmation == JOptionPane.YES_OPTION) {
        SuppDAO suppDAO;
         try {
         suppDAO = new SuppDAO();
         suppDAO.deleteSupplier(selectedSuppId);
         JOptionPane.showMessageDialog(this, "Supplier Deleted successfully ", "Success", JOptionPane.INFORMATION_MESSAGE);
         this.clearForm();
         loadSupplierData();
         selectedSuppId = -1;
         } catch (SQLException ex){
        JOptionPane.showMessageDialog(this, "Error occurred while deletting", "Error", JOptionPane.ERROR_MESSAGE);
    }
        }
   }else {
            JOptionPane.showMessageDialog(this, "No Supplier Selected for Delete ", "Error", JOptionPane.ERROR_MESSAGE);
       }
    }//GEN-LAST:event_del_suppActionPerformed

