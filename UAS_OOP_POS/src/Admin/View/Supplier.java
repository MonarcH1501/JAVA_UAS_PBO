    private void search_suppKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_search_suppKeyReleased
    String sr_supp = search_supp.getText();
    String selectedColumn = cBox_supp.getSelectedItem().toString();
    String DBColumn = "";
    switch (selectedColumn) {
        case "ID":
            DBColumn = "id_supplier";
            break;
        case "Kode Supplier":
            DBColumn = "supp_code";
            break;
        case "Nama Supplier":
            DBColumn = "supp_name";
            break;
        case "Contact":
            DBColumn = "contact";
            break;
        case "Address":
            DBColumn = "address";
            break;
        default:
        DBColumn = "id_supplier";
    }
        try {
        if(sr_supp.isEmpty()) {
            loadSupplierData();
        } else {
        SuppDAO suppDAO = new SuppDAO();
        List<Supp> suppliers = suppDAO.searchSuppliers(sr_supp, DBColumn);
        
        String[][] data = new String[suppliers.size()][5];
        
        for(int i = 0; i < suppliers.size(); i++) {
        data[i][0] = String.valueOf(suppliers.get(i).getId());
        data[i][1] = suppliers.get(i).getCodeSupplier();
        data[i][2] = suppliers.get(i).getNameSupplier();
        data[i][3] = suppliers.get(i).getContactSupplier();
        data[i][4] = suppliers.get(i).getAddressSupplier();
    }
        String[] columnName = {"ID", "Kode Supplier", "Nama Supplier", "Contact", "Address"};
        table_supp.setModel(new javax.swing.table.DefaultTableModel(data, columnName));
    } }
    catch (SQLException ex){
        JOptionPane.showMessageDialog(this, "Error occurred while searching", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_search_suppKeyReleased

